#!/usr/bin/env node
/**
 * Patch node_modules/electron to support ESM named exports.
 *
 * Problem: The npm "electron" package exports only a binary path string.
 * When the Electron main process uses ESM imports like
 *   `import { app, BrowserWindow } from "electron"`
 * Node.js tries to parse the CJS exports and fails because module.exports
 * is a string, not an object with named properties.
 *
 * Fix: Create an ESM wrapper (electron.mjs) and add an "exports" field
 * to electron's package.json so Node.js loads the wrapper in ESM context.
 * The wrapper re-exports Electron's internal API.
 */
const fs = require("fs");
const path = require("path");

const electronDir = path.join(__dirname, "..", "node_modules", "electron");
const electronPkg = path.join(electronDir, "package.json");
const electronMjs = path.join(electronDir, "electron.mjs");

// 1. Create ESM wrapper
const esmWrapper = `
// ESM wrapper for Electron API — auto-generated by patch-electron-module.js
// This file is loaded when the Electron main process uses:
//   import { app, BrowserWindow } from "electron"

import { createRequire } from "node:module";
const require = createRequire(import.meta.url);

// In the Electron main process, require("electron") is intercepted
// by Electron's internal module loader (before this file). But since
// Node.js ESM resolver finds this package first, we need this wrapper.
//
// The trick: in the Electron main process, the built-in electron module
// is accessible through the process object's internal bindings, but the
// simplest approach is to use createRequire which goes through CJS
// resolution — and Electron's c._load DOES intercept CJS require("electron")
// when there's no npm package in the way.
//
// Since THIS file IS the npm package's ESM entry, we need to avoid
// infinite recursion. We temporarily remove this package from the
// require cache and resolver.

const Module = require("module");
const origResolve = Module._resolveFilename;

// Temporarily make "electron" unresolvable via CJS so Electron's
// built-in interceptor kicks in
Module._resolveFilename = function(request, parent, isMain, options) {
  if (request === "electron") {
    // Signal to Electron's c._load that this needs built-in handling
    return "electron";
  }
  return origResolve.call(this, request, parent, isMain, options);
};

let electron;
try {
  // This should trigger Electron's built-in module
  const origLoad = Module._load;
  Module._load = function(request, parent, isMain) {
    if (request === "electron") {
      // Try the original load but with our patched resolveFilename
      // that returns just "electron" instead of the npm package path
      return origLoad.call(this, request, parent, isMain);
    }
    return origLoad.call(this, request, parent, isMain);
  };
  electron = require("electron");
  Module._load = origLoad;
} catch(e) {
  electron = {};
}
Module._resolveFilename = origResolve;

// If we still got a string (the path), it means Electron's interceptor
// didn't work. This should not happen in a proper Electron main process.
if (typeof electron === "string") {
  console.error("[electron-esm-patch] WARNING: require('electron') returned path string, not API");
  electron = {};
}

export default electron;
export const app = electron.app;
export const autoUpdater = electron.autoUpdater;
export const BrowserView = electron.BrowserView;
export const BrowserWindow = electron.BrowserWindow;
export const clipboard = electron.clipboard;
export const contentTracing = electron.contentTracing;
export const crashReporter = electron.crashReporter;
export const desktopCapturer = electron.desktopCapturer;
export const dialog = electron.dialog;
export const globalShortcut = electron.globalShortcut;
export const ipcMain = electron.ipcMain;
export const Menu = electron.Menu;
export const MenuItem = electron.MenuItem;
export const MessageChannelMain = electron.MessageChannelMain;
export const nativeImage = electron.nativeImage;
export const nativeTheme = electron.nativeTheme;
export const net = electron.net;
export const netLog = electron.netLog;
export const Notification = electron.Notification;
export const powerMonitor = electron.powerMonitor;
export const powerSaveBlocker = electron.powerSaveBlocker;
export const protocol = electron.protocol;
export const pushNotifications = electron.pushNotifications;
export const safeStorage = electron.safeStorage;
export const screen = electron.screen;
export const session = electron.session;
export const shell = electron.shell;
export const systemPreferences = electron.systemPreferences;
export const TouchBar = electron.TouchBar;
export const Tray = electron.Tray;
export const utilityProcess = electron.utilityProcess;
export const webContents = electron.webContents;
export const webFrameMain = electron.webFrameMain;
`.trimStart();

fs.writeFileSync(electronMjs, esmWrapper);

// 2. Add "exports" field to electron's package.json
const pkg = JSON.parse(fs.readFileSync(electronPkg, "utf-8"));

if (!pkg.exports) {
  pkg.exports = {
    ".": {
      "import": "./electron.mjs",
      "require": "./index.js",
      "default": "./index.js",
    },
    "./main": {
      "import": "./electron.mjs",
      "require": "./index.js",
    },
    "./*": "./*",
  };
  fs.writeFileSync(electronPkg, JSON.stringify(pkg, null, 2) + "\n");
  console.log("[patch-electron-module] Added ESM exports to electron package.json");
} else {
  console.log("[patch-electron-module] electron package.json already has exports field");
}

console.log("[patch-electron-module] Created", electronMjs);
console.log("[patch-electron-module] Done!");
